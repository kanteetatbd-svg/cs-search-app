# üöÄ [RESTORED SECTION] ‡∏£‡∏∞‡∏ö‡∏ö Smart Header Logic ‡πÅ‡∏•‡∏∞‡∏Å‡∏≤‡∏£‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå (‡∏´‡πâ‡∏≤‡∏°‡∏•‡∏ö‡πÄ‡∏î‡πá‡∏î‡∏Ç‡∏≤‡∏î)
@st.cache_data(ttl=3600)
def load_data_from_file(filename):
    gc = get_sheets_client()
    try:
        sh = gc.open(filename)
        all_tabs = {}
        for ws in sh.worksheets():
            data = ws.get_all_values()
            if not data: continue
            df = pd.DataFrame(data)
            
            # --- ‡∏ï‡∏£‡∏£‡∏Å‡∏∞‡∏Å‡∏≤‡∏£‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡πÅ‡∏ñ‡∏ß‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡πá‡∏ô‡∏´‡∏±‡∏ß‡∏ï‡∏≤‡∏£‡∏≤‡∏á (Smart Header) ---
            header_idx = 0
            for i in range(min(15, len(df))):
                # ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡πÅ‡∏ñ‡∏ß‡πÑ‡∏´‡∏ô‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡∏ô‡∏≤‡πÅ‡∏ô‡πà‡∏ô‡∏û‡∏≠ (‡∏°‡∏≤‡∏Å‡∏Å‡∏ß‡πà‡∏≤ 5 ‡∏ä‡πà‡∏≠‡∏á)
                non_empty_count = 0
                for x in df.iloc[i]:
                    if str(x).strip() != "":
                        non_empty_count += 1
                
                if non_empty_count > 5:
                    header_idx = i
                    break
            
            # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡∏±‡∏ß‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏°‡∏≤‡∏ó‡∏≥‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏∞‡∏≠‡∏≤‡∏î
            headers = df.iloc[header_idx].astype(str).tolist()
            
            # --- ‡∏ï‡∏£‡∏£‡∏Å‡∏∞‡∏Å‡∏≤‡∏£‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏ä‡∏∑‡πà‡∏≠‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå‡∏ã‡πâ‡∏≥‡∏´‡∏£‡∏∑‡∏≠‡∏ß‡πà‡∏≤‡∏á (Duplicate Handler) ---
            final_headers = []
            for i, h in enumerate(headers):
                clean_h = h.strip()
                if not clean_h or clean_h in final_headers:
                    # ‡∏ñ‡πâ‡∏≤‡∏ä‡∏∑‡πà‡∏≠‡∏ß‡πà‡∏≤‡∏á‡∏´‡∏£‡∏∑‡∏≠‡∏ã‡πâ‡∏≥ ‡πÉ‡∏´‡πâ‡∏ï‡∏±‡πâ‡∏á‡∏ä‡∏∑‡πà‡∏≠‡πÄ‡∏õ‡πá‡∏ô Column_X
                    final_headers.append(f"Column_{i+1}")
                else:
                    final_headers.append(clean_h)
            
            # ‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏•‡∏Ç‡πÅ‡∏ñ‡∏ß‡∏à‡∏£‡∏¥‡∏á‡∏à‡∏≤‡∏Å Google Sheets (Sheet Row ID)
            df['sheet_row'] = df.index + 1
            df.columns = final_headers + ['sheet_row']
            
            # ‡∏ï‡∏±‡∏î‡∏™‡πà‡∏ß‡∏ô‡∏´‡∏±‡∏ß‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡πá‡∏ô‡∏Ç‡∏¢‡∏∞‡∏≠‡∏≠‡∏Å ‡πÅ‡∏•‡∏∞‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏£‡∏¥‡∏á‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏à‡∏≤‡∏Å‡πÉ‡∏ï‡πâ Header
            all_tabs[ws.title] = df.iloc[header_idx+1:].reset_index(drop=True)
            
        return all_tabs
    except Exception as e:
        st.error(f"Error Loading '{filename}': {e}")
        return None

