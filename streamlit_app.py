# --- 1. ‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠ Google Sheets ---
@st.cache_resource
def get_sheets_client():
    try:
        scopes = ['https://www.googleapis.com/auth/spreadsheets', 'https://www.googleapis.com/auth/drive']
        creds = Credentials.from_service_account_file('key.json', scopes=scopes)
        return gspread.authorize(creds)
    except Exception as e:
        st.error(f"‚ùå ‡∏£‡∏∞‡∏ö‡∏ö‡∏´‡∏≤‡∏Å‡∏∏‡∏ç‡πÅ‡∏à‡πÑ‡∏°‡πà‡πÄ‡∏à‡∏≠: {e}")
        return None

# --- 2. ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• (‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Å‡∏≤‡∏£‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏•‡∏Ç‡πÅ‡∏ñ‡∏ß‡πÑ‡∏ß‡πâ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç) ---
@st.cache_data(ttl=60) # ‡∏ï‡∏±‡πâ‡∏á‡πÅ‡∏Ñ‡∏ä‡∏™‡∏±‡πâ‡∏ô‡∏•‡∏á‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡πÄ‡∏´‡πá‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏µ‡πà‡πÅ‡∏Å‡πâ‡πÄ‡∏£‡πá‡∏ß‡∏Ç‡∏∂‡πâ‡∏ô
def load_data_for_edit():
    gc = get_sheets_client()
    if not gc: return {}
    sh = gc.open('Copy of ‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏Ñ‡∏™2025V1')
    worksheets = sh.worksheets()
    all_data = {}
    
    for ws in worksheets:
        data = ws.get_all_values()
        if not data: continue
        df = pd.DataFrame(data)
        
        # ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡∏´‡∏±‡∏ß‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô‡πÄ‡∏î‡∏¥‡∏°
        header_idx = 0
        max_cols = 0
        for i in range(min(15, len(df))):
            count = sum(1 for x in df.iloc[i] if str(x).strip() != "")
            if count > max_cols:
                max_cols = count
                header_idx = i
        
        headers = df.iloc[header_idx].tolist()
        # ‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏•‡∏Ç‡πÅ‡∏ñ‡∏ß‡∏à‡∏£‡∏¥‡∏á‡πÉ‡∏ô Sheets ‡πÑ‡∏ß‡πâ (‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç‡∏°‡∏≤‡∏Å‡∏ï‡∏≠‡∏ô‡∏à‡∏∞‡πÅ‡∏Å‡πâ)
        df['sheet_row'] = df.index + 1 
        df.columns = headers + ['sheet_row']
        
        # ‡∏ï‡∏±‡∏î‡∏™‡πà‡∏ß‡∏ô Dashboard ‡πÅ‡∏•‡∏∞‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏ô‡∏∑‡πâ‡∏≠‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
        df = df.iloc[header_idx+1:].reset_index(drop=True)
        all_data[ws.title] = df
    return all_data

# --- 3. ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏ó‡∏µ‡πà Sheets ---
def update_sheet(ws_title, row_index, col_name, new_value):
    try:
        gc = get_sheets_client()
        sh = gc.open('Copy of ‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏Å‡πá‡∏ö‡πÄ‡∏Ñ‡∏™2025V1')
        ws = sh.worksheet(ws_title)
        
        # ‡∏´‡∏≤‡∏ß‡πà‡∏≤‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡πÅ‡∏Å‡πâ‡∏≠‡∏¢‡∏π‡πà‡∏•‡∏≥‡∏î‡∏±‡∏ö‡∏ó‡∏µ‡πà‡πÄ‡∏ó‡πà‡∏≤‡πÑ‡∏´‡∏£‡πà
        headers = ws.row_values(ws.find(ws_title).row if not ws.row_values(1) else 1) # ‡∏´‡∏≤‡∏´‡∏±‡∏ß‡∏ï‡∏≤‡∏£‡∏≤‡∏á
        # ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ä‡∏±‡∏ß‡∏£‡πå ‡πÄ‡∏£‡∏≤‡∏à‡∏∞‡∏Ç‡∏≠‡∏´‡∏±‡∏ß‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏•‡πà‡∏≤‡∏™‡∏∏‡∏î
        all_headers = ws.row_values(1) # ‡∏™‡∏°‡∏°‡∏ï‡∏¥‡∏´‡∏±‡∏ß‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏ñ‡∏ß 1 (‡∏´‡∏£‡∏∑‡∏≠‡∏ï‡∏≤‡∏°‡∏£‡∏∞‡∏ö‡∏ö Smart Header)
        # ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡πÅ‡∏ö‡∏ö‡∏£‡∏∞‡∏ö‡∏∏‡∏û‡∏¥‡∏Å‡∏±‡∏î [‡πÅ‡∏ñ‡∏ß, ‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå]
        col_idx = all_headers.index(col_name) + 1
        ws.update_cell(row_index, col_idx, new_value)
        return True
    except:
        return False

